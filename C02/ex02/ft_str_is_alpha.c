#include <unistd.h>

int	ft_str_is_alpha(char *str)
{
	int		i;

	if (str[0] == '\0')
		return (1);
	i = 0;
	while (str[i] != '\0')
	{
		if ((65 > str[i] || str[i] > 90) && (97 > str[i] || str[i] > 122))
			return (0);
		i++;
	}
	return (1);
}
